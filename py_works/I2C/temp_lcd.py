import smbus
from time import sleep
from lcd import setup_st7032, clear, newline, write_string
from temp import read_adt7410

setup_st7032()

try:
    while True:
        inputValue = read_adt7410()
        try:
            clear()
            s = str(inputValue)
            write_string(s+chr(0xdf))
        except IOError:
            print("接続エラースキップ")
        sleep(1)
except KeyboardInterrupt:
    clear()
    pass